export default function EmojiPicker({ value, onChange, onClose }) {
  const [search, setSearch] = useState('');
  const containerRef = useRef(null);
  const categoryRefs = useRef({});

  // Close picker when clicking outside
  useEffect(() => {
    const handleClickOutside = (e) => {
      if (containerRef.current && !containerRef.current.contains(e.target)) {
        onClose();
      }
    };
    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, [onClose]);

  const scrollToCategory = (category, e) => {
    e.preventDefault();
    e.stopPropagation();
    categoryRefs.current[category]?.scrollIntoView({ behavior: 'smooth', block: 'start' });
  };

  const handleEmojiClick = (emoji, e) => {
    e.preventDefault();
    e.stopPropagation();
    onChange(emoji);
    onClose();
  };

  // Filter emojis by search term
  const getFilteredData = () => {
    if (!search) return EMOJI_DATA;

    const searchLower = search.toLowerCase();
    const results = [];

    for (const [category, emojis] of Object.entries(EMOJI_DATA)) {
      for (const [emoji, keywords] of emojis) {
        if (keywords.toLowerCase().includes(searchLower)) {
          results.push([emoji, keywords]);
        }
      }
    }

    return results.length > 0 ? { 'Results': results } : { 'Results': [] };
  };

  const filteredData = getFilteredData();

  return (
    <div
      ref={containerRef}
      className="bg-white rounded-lg shadow-xl border border-gray-200 w-80 max-h-96 flex flex-col"
      onClick={(e) => e.stopPropagation()}
    >
      {/* Search */}
      <div className="p-2 border-b border-gray-100">
        <input
          type="text"
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          placeholder="Search emoji... (pizza, smile, heart)"
          className="w-full px-3 py-1.5 text-sm border border-gray-200 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-transparent"
          autoFocus
          onClick={(e) => e.stopPropagation()}
        />
      </div>

      {/* Category jump links */}
      {!search && (
        <div className="flex gap-1 p-2 border-b border-gray-100 overflow-x-auto flex-shrink-0">
          {categories.map(cat => (
            <button
              key={cat}
              type="button"
              onClick={(e) => scrollToCategory(cat, e)}
              className="px-2 py-1 text-xs rounded whitespace-nowrap hover:bg-gray-100 text-gray-600 transition-colors"
            >
              {cat}
            </button>
          ))}
        </div>
      )}

      {/* All emojis with category headers */}
      <div className="flex-1 overflow-y-auto p-2">
        {Object.entries(filteredData).map(([category, emojis]) => (
          <div key={category} ref={el => categoryRefs.current[category] = el}>
            <h4 className="text-xs font-semibold text-gray-500 uppercase tracking-wider mb-2 mt-2 first:mt-0">
              {category}
            </h4>
            <div className="grid grid-cols-8 gap-1 mb-2">
              {emojis.map(([emoji, keywords], i) => (
                <button
                  key={`${emoji}-${i}`}
                  type="button"
                  onClick={(e) => handleEmojiClick(emoji, e)}
                  title={keywords}
                  className={`w-8 h-8 flex items-center justify-center text-xl hover:bg-gray-100 rounded transition-colors ${
                    value === emoji ? 'bg-blue-100' : ''
                  }`}
                >
                  {emoji}
                </button>
              ))}
            </div>
          </div>
        ))}
        {search && filteredData['Results']?.length === 0 && (
          <p className="text-sm text-gray-400 text-center py-4">No emoji found for "{search}"</p>
        )}
      </div>
    </div>
  );
}
